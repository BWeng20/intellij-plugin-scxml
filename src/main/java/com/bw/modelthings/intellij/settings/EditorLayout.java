package com.bw.modelthings.intellij.settings;

/**
 * Editor layout mode.
 */
public enum EditorLayout
{
	/**
	 * XMl and Graph side by side, with vertical split.
	 */
	SplitVertical,

	/**
	 * XMl and Graph side by side, with horizontal split.
	 */
	SplitHorizontal,

	/**
	 * XMl and Graph in separate tabs.
	 */
	Tabs

}
